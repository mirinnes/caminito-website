// Proyects
class Proyect {
	constructor(id, title, subTitle, description, images, tapa) {
		this.id = id;
		this.title = title;
		this.subTitle = subTitle;
		this.description = description;
		this.images = images;
		this.tapa = tapa;
	}
}

// Portfolios
class Portfolio {
	constructor(id, title, proyects) {
		this.id = id;
		this.title = title;
		this.proyects = proyects;
		this.showProyects = false;
	}

	setShowProyects = (state) => {
		this.showProyects = state;
	};
}

